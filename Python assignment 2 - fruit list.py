# Step 1: Create a list of 10 fruits
fruits = ["apple", "orange", "banana", "kiwi", "grape", "pear", "mango", "pineapple", "cherry", "strawberry"]

# Step 2: Alphabetize the list
fruits.sort()

# Step 3: Display the alphabetized list
print("Alphabetized list of fruits:")
print(fruits)
print("Alphabetized list of fruits:")
for fruit in fruits:
    print(fruit)
